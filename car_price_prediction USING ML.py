# ðŸš— Enhanced Car Price Prediction with Advanced Features - Single Cell

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# Load data
df = pd.read_csv('car data.csv')

# Feature engineering
df['Car_Age'] = 2025 - df['Year']
df['Kms_per_Year'] = df['Kms_Driven'] / df['Car_Age'].replace(0, 1)
df['Kms_Driven_log'] = np.log1p(df['Kms_Driven'])

# Encode categorical variables
df.replace({'Fuel_Type': {'Petrol': 0, 'Diesel': 1, 'CNG': 2},
            'Seller_Type': {'Dealer': 0, 'Individual': 1},
            'Transmission': {'Manual': 0, 'Automatic': 1}}, inplace=True)

# Define features and target
features = ['Present_Price', 'Fuel_Type', 'Seller_Type', 'Transmission', 'Owner',
            'Car_Age', 'Kms_per_Year', 'Kms_Driven_log']
X = df[features]
y = df['Selling_Price']

# Train-test split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.1, random_state=42)

# Model training
model = RandomForestRegressor(n_estimators=100, random_state=42)
model.fit(X_train, y_train)

# Predictions
y_pred = model.predict(X_test)

# Evaluation metrics
print("ðŸ“ˆ R2 Score:", round(r2_score(y_test, y_pred), 3))
print("ðŸ“‰ MAE:", round(mean_absolute_error(y_test, y_pred), 3))
print("ðŸ“Š RMSE:", round(np.sqrt(mean_squared_error(y_test, y_pred)), 3))

# Feature importance visualization
importances = pd.Series(model.feature_importances_, index=X.columns)
plt.figure(figsize=(8, 5))
importances.sort_values().plot(kind='barh', title='ðŸ” Feature Importances')
plt.xlabel('Importance Score')
plt.tight_layout()
plt.show()

# Outlier detection (based on large residuals)
errors = y_test - y_pred
outliers = errors[np.abs(errors) > 2]
print(f"\nðŸš¨ Outlier Alert: {len(outliers)} predictions deviated by > 2 Lakhs")

# Top undervalued & overvalued predictions
comparison = pd.DataFrame({'Actual': y_test, 'Predicted': y_pred, 'Error': errors})
print("\nðŸ’¸ Top 5 Most Undervalued Cars (Model underestimates value):")
print(comparison.sort_values(by='Error', ascending=False).head(5))

print("\nðŸ§¾ Top 5 Most Overvalued Cars (Model overestimates value):")
print(comparison.sort_values(by='Error').head(5))

